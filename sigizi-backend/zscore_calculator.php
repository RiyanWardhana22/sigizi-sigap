     * @param string $jenis_kelamin 'L' atau 'P'
     * @return array|null Parameter LMS
     */
    public static function getLMSParameters($indikator, $umur_bulan, $jenis_kelamin) {
        $umur = round($umur_bulan);
        $umur = max(0, min(60, $umur));
        
        if ($indikator == 'TB_U') {
            $data = ($jenis_kelamin == 'L') ? self::$tb_u_laki : self::$tb_u_perempuan;
        } elseif ($indikator == 'BB_U') {
            $data = ($jenis_kelamin == 'L') ? self::$bb_u_laki : self::$bb_u_perempuan;
        } else {
            return null;
        }
        
        return isset($data[$umur]) ? $data[$umur] : null;
    }
    
    /**
     * Menghitung Z-Score menggunakan metode LMS
     * Formula: Z = ((X/M)^L - 1) / (L * S) untuk L != 0
     *          Z = ln(X/M) / S untuk L = 0
     * @param float $nilai Nilai yang diukur (tinggi atau berat)
     * @param float $L Parameter lambda
     * @param float $M Parameter mu (median)
     * @param float $S Parameter sigma
     * @return float|null Z-Score
     */
    public static function hitungZScore($nilai, $L, $M, $S) {
        if ($nilai <= 0 || $M <= 0) return null;
        
        if (abs($L) < 0.0001) {
            // Box-Cox transformation with L = 0 (log transformation)
            $z = log($nilai / $M) / $S;
        } else {
            // Box-Cox transformation
            $z = (pow($nilai / $M, $L) - 1) / ($L * $S);
        }
        
        return round($z, 2);
    }
    
    /**
     * Mendapatkan status gizi berdasarkan Z-Score dan indikator
     * @param float|null $z_score
     * @param string $indikator 'TB_U' atau 'BB_U'
     * @return string Status gizi
     */
    public static function getStatusGizi($z_score, $indikator) {
        if ($z_score === null) return 'Tidak Terdefinisi';
        
        if ($indikator == 'TB_U') { // Stunting
            if ($z_score < -3) return 'Sangat Pendek (Severe Stunting)';
            if ($z_score < -2) return 'Pendek (Stunting)';
            if ($z_score > 2) return 'Tinggi';
            return 'Normal';
        } 
        elseif ($indikator == 'BB_U') { // Underweight
            if ($z_score < -3) return 'Berat Badan Sangat Kurang (Severe Underweight)';
            if ($z_score < -2) return 'Berat Badan Kurang (Underweight)';
            if ($z_score > 2) return 'Berat Badan Lebih (Overweight)';
            if ($z_score > 3) return 'Obesitas';
            return 'Berat Badan Normal';
        }
        
        return 'Tidak Terdefinisi';
    }
    
    /**
     * Analisis lengkap status gizi anak
     * @param string $tanggal_lahir
     * @param string $jenis_kelamin 'L' atau 'P'
     * @param float $tinggi_badan cm
     * @param float $berat_badan kg
     * @param string|null $tanggal_ukur
     * @return array Hasil analisis
     */
    public static function analisisLengkap($tanggal_lahir, $jenis_kelamin, $tinggi_badan, $berat_badan, $tanggal_ukur = null) {
        // Hitung umur
        $umur_bulan = self::hitungUmurBulan($tanggal_lahir, $tanggal_ukur);
        
        // Analisis TB/U (Stunting)
        $params_tb_u = self::getLMSParameters('TB_U', $umur_bulan, $jenis_kelamin);
        $z_score_tb_u = null;
        $status_stunting = 'Tidak Terdefinisi';
        
        if ($params_tb_u) {
            $z_score_tb_u = self::hitungZScore($tinggi_badan, $params_tb_u['L'], $params_tb_u['M'], $params_tb_u['S']);
            $status_stunting = self::getStatusGizi($z_score_tb_u, 'TB_U');
        }
        
        // Analisis BB/U (Underweight)
        $params_bb_u = self::getLMSParameters('BB_U', $umur_bulan, $jenis_kelamin);
        $z_score_bb_u = null;
        $status_underweight = 'Tidak Terdefinisi';
        
        if ($params_bb_u) {
            $z_score_bb_u = self::hitungZScore($berat_badan, $params_bb_u['L'], $params_bb_u['M'], $params_bb_u['S']);
            $status_underweight = self::getStatusGizi($z_score_bb_u, 'BB_U');
        }
        
        // Tentukan status keseluruhan (prioritas: Stunting lebih berbahaya dari Underweight)
        $status_keseluruhan = 'Normal';
        $z_score_utama = 0;
        
        if (strpos($status_stunting, 'Stunting') !== false) {
            $status_keseluruhan = $status_stunting;
            $z_score_utama = $z_score_tb_u;
        } elseif (strpos($status_underweight, 'Kurang') !== false || strpos($status_underweight, 'Lebih') !== false) {
            $status_keseluruhan = $status_underweight;
            $z_score_utama = $z_score_bb_u;
        } else {
            $status_keseluruhan = 'Normal';
            $z_score_utama = max($z_score_tb_u ?? -100, $z_score_bb_u ?? -100);
        }
        
        // Simplified status untuk database
        $simplified_status = 'Normal';
        if (strpos($status_stunting, 'Stunting') !== false) {
            $simplified_status = 'Stunting';
        } elseif (strpos($status_underweight, 'Kurang') !== false) {
            $simplified_status = 'Gizi Kurang';
        } elseif (strpos($status_underweight, 'Lebih') !== false || strpos($status_underweight, 'Obesitas') !== false) {
            $simplified_status = 'Gizi Lebih';
        }
        
        return [
            'umur_bulan' => round($umur_bulan, 1),
            'tb_u' => [
                'z_score' => $z_score_tb_u,
                'status' => $status_stunting
            ],
            'bb_u' => [
                'z_score' => $z_score_bb_u,
                'status' => $status_underweight
            ],
            'status_keseluruhan' => $status_keseluruhan,
            'simplified_status' => $simplified_status,
            'z_score_utama' => $z_score_utama
        ];
    }
