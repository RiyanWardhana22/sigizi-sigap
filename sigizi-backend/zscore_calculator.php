    /**
     * Menghitung umur dalam bulan dengan presisi (termasuk hari)
     * @param string $tanggal_lahir Format Y-m-d
     * @param string|null $tanggal_ukur Format Y-m-d (default: hari ini)
     * @return float Umur dalam bulan
     */
    public static function hitungUmurBulan($tanggal_lahir, $tanggal_ukur = null) {
        $lahir = new DateTime($tanggal_lahir);
        $ukur = $tanggal_ukur ? new DateTime($tanggal_ukur) : new DateTime();
        $diff = $lahir->diff($ukur);
        
        $umur_bulan = $diff->y * 12 + $diff->m;
        $umur_hari = $diff->d;
        
        // Konversi ke bulan dengan desimal (1 bulan = 30.44 hari)
        return $umur_bulan + ($umur_hari / 30.44);
    }
    
